package sectionTwoOOPs;

public class typesOfVariables {
	
//	1 Local Variables:
//		Declared within a method, constructor, or block.
//		Scope is limited to the block in which they are declared.
//		They must be initialized before use.
	
//	2 Instance Variables (Non-Static Fields):
//      Declared within a class but outside any method, constructor, or block.
//		Each instance of the class has its own copy of instance variables.
//		They are initialized with default values.

//	3 Class Variables (Static Fields):
//		Declared with the static keyword within a class but outside any method, constructor, or block.
//		Shared among all instances of the class.
//		They are initialized with default values.
	
//	4 Parameters (Method Parameters):
// 		Variables declared in the method signature.
//		They receive values when the method is called.
//		They act as local variables within the method.
	
//	5 Final Variables (Constants):
//		Declared with the final keyword.
//		Their value cannot be changed after initialization.
//		They can be class variables, instance variables, or local variables.

	

}
